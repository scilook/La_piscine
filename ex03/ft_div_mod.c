void	ft_div_mod(int a, int b, int *div, int *mod)
{
	*div = a / b;
	*mod = a % b;
}

int main()
{
	int a = 5, b = 10;
	ft_div_mod(a, b, &a, &b);
	printf("%d, %d", a, b);
}