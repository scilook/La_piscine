int	ft_str_is_numeric(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= 0x30 && str[i] <= 0x39)
			i++;
		else
			return (0);
	}
	return (1);
}
