int	ft_str_is_alpha(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= 'A' && str[i] <= 'z')
		{
			if (str[i] >= 'a' || str[i] <= 'Z')
				i++;
			else
				return (0);
		}
		else
			return (0);
	}
	return (1);
}
