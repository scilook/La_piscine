#include <unistd.h>

void	ft_print_comb(void)
{
	char	i;
	char	j;
	char	k;

	i = 0x30;
	while (i < 0x3A)
	{
		j = i + 1;
		while (j < 0x3A)
		{
			k = j + 1;
			while (k < 0x3A)
			{
				write(1, &i, 1);
				write(1, &j, 1);
				write(1, &k, 1);
				if (i * j * k != 0x37 * 0x38 * 0x39)
					write(1, ", ", 2);
				k++;
			}
			j++;
		}
		i++;
	}
}
